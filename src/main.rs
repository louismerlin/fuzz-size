fn main() {
    afl::fuzz!(|data: &[u8]| {
        match data.len() {
            0..=10 => {
                println!("0..=10");
            },
            11..=100 => {
                println!("11..=100");
            },
            101..=1_000 => {
                println!("101..=1_000");
            },
            1_001..=10_000 => {
                println!("1_001..=10_000");
            },
            10_001..=100_000 => {
                println!("10_001..=100_000");
            },
            100_001..=1_000_000 => {
                println!("100_001..=1_000_000");
            },
            1_000_001..=2_000_000 => {
                println!("1_000_001..=2_000_000");
            },
            _ => {
                panic!("Input size is sufficiently large");
            },
        }
    });
}
